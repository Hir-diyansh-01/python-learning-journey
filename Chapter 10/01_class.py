class employee:
    language = "Python"
    experience = 5
    salary = 5000000

hirdiyansh = employee()
hirdiyansh.name = "Hirdiyansh"
print(hirdiyansh.name)
print(hirdiyansh.language)
print(hirdiyansh.experience)
print(hirdiyansh.salary)

ram = employee()
ram.name = "Ram"
print(ram.name)
print(ram.language)
print(ram.experience)
print(ram.salary)

# Here name is object variable and language, experience, salary are class variables.