# Arithmetic operations in Python
# Addition
a = 10                      
b = 5
c = 15
d = a / b
addition = a + b
print("Addition:", addition)

# Subtraction
subtraction = a - b
print("Subtraction:", subtraction)

# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)

# Division
division = a / b
print("Division:", division)

# Assignment operators
a += 5  # Equivalent to a = a + 5
print("After addition assignment (a += 5):", a) 
b *= 2  # Equivalent to b = b * 2
print("After multiplication assignment (b *= 2):", b)
c -= 3  # Equivalent to c = c - 3
print("After subtraction assignment (c -= 3):", c)
d /= 2  # Equivalent to d = d / 2
print("Division assignment (d = a / b, then d /= 2):", d)

#Comparison operators
print("Is a greater than b?", a > b)
print("Is a less than b?", a < b)
print("Is a not equal to b?", a != b)
print("Is a equal to b?", a == b)
print("Is a equal to a?", a == a)

# Logical operators
x = True
y = False
print("Logical AND (x and y):", x and y)
print("Logical OR (x or y):", x or y)
print("Logical NOT (not x):", not x)   

