a = 10  
t = type(a)
print(t)  # <class 'int'>

b = 3.14
t = type(b)
print(t)  # <class 'float'>

c = "Hello"
t = type(c)
print(t)  # <class 'str'>

# Type conversion
x = 5
y = float(x)  # Convert int to float
print(y)  # 5.0

z = str(x)  # Convert int to str
print(z)  # '5'

w = int(y)  # Convert float to int
print(w)  # 5

